package com.entity;

public class RelevantInformationEntity {

    private Integer relevantInformationId;

    private String relevantInformationPicture;

    private String relevantInformationTitle;

    private String relevantInformationContent;

    private String releaseTime;

    public Integer getRelevantInformationId() {
        return relevantInformationId;
    }

    public void setRelevantInformationId(Integer relevantInformationId) {
        this.relevantInformationId = relevantInformationId;
    }

    public String getRelevantInformationPicture() {
        return relevantInformationPicture;
    }

    public void setRelevantInformationPicture(String relevantInformationPicture) {
        this.relevantInformationPicture = relevantInformationPicture;
    }

    public String getRelevantInformationTitle() {
        return relevantInformationTitle;
    }

    public void setRelevantInformationTitle(String relevantInformationTitle) {
        this.relevantInformationTitle = relevantInformationTitle;
    }

    public String getRelevantInformationContent() {
        return relevantInformationContent;
    }

    public void setRelevantInformationContent(String relevantInformationContent) {
        this.relevantInformationContent = relevantInformationContent;
    }

    public String getReleaseTime() {
        return releaseTime;
    }

    public void setReleaseTime(String releaseTime) {
        this.releaseTime = releaseTime;
    }
}
